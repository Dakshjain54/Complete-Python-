'''
 Typecasting 
 the conversion of one data type into the data type is known as typecasting in python or conversion in python
 functions like int(), float(), str(), ord(), tuple(), etc
 TWO TYPES OF TYPECASTING
 1. Explicit Conversion
 2. Implicit Conversion
'''

#  1. Explicit Conversion
a = "1"
b = "2"
print(int(a)+int(b))

#  2. Implicit Conversion
# one data type is convertedd into other by the python interpreter itself (automatically)
c = 1.9
d = 8
print(c + d)
