# Strings are immutable
a = "daksh jain !!! "
print(a)
# 1.Upper():
print(a.upper())

# # # 2.lower():
print(a.lower())

# # 3.rstrip():
# print(a.rstrip("!"))

# # 4.replace():
print(a.replace("daksh", "Ram"))

# # 5.split():
print(a.split(" "))

# # 6.capitalize():
print(a.capitalize())

# # 7.center():
print(a.center(50))

# # 8.count():
print(a.count("!"))

# # 9.endswith():
print(a.endswith("!!!"))

# # 10.find():
print(a.find("n"))

# # 11.index():
# print(a.index("nn"))

# # 12.isalnum():
print(a.isalnum())

# # 13.islower():
print(a.islower())

# # 14.isspace():
print(a.isspace())